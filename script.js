function openNav() {
    document.getElementById("myMenu").style.width = "200px";/*to open the menu*/
    document.getElementById("myMenu").style.marginLeft = "200px";
}

function closeNav() {
    document.getElementById("myMenu").style.width = "0px";/*remove menu*/
    document.getElementById("myMenu").style.marginLeft = "750px";/*to make it slide away from the users view*/
}
